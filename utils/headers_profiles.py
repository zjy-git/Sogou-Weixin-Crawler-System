def random_headers_profile() -> Tuple[str, Dict[str, str]]:
    profile = random.choice(HEADER_PROFILES).copy()
    user_agent = random.choice(USER_AGENTS)
    profile["User-Agent"] = user_agent
    return user_agent, profile
